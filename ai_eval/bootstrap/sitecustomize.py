"""Generate the bootstrap ``sitecustomize.py`` shim.

Python auto-imports ``sitecustomize`` at interpreter startup if it's on
``sys.path``. The parent writes the shim to a temp dir, prepends that dir to
``PYTHONPATH`` of the child subprocess, and points the shim at a JSONL sink
path (via env var) the parent reads after the child exits.

The shim:
- imports :func:`ai_eval.bootstrap.wrappers.install_hooks`,
- wires the capture sink to an append-only JSONL writer at ``$AI_EVAL_BOOTSTRAP_SINK``,
- wraps the user's task entry functions (best-effort; the parent passes the
  task specs via env so the shim knows which callables to wrap as top-level
  tasks).

Wrapping top-level tasks is the hardest part: we don't know the user's entry
symbol until their module imports. The shim instead installs a
``sys.settrace``-light approach: it records LLM/retrieve calls into the active
task, and treats each *test function* boundary (pytest item) as a task. For
the general case (arbitrary command), the shim records framework calls under
a synthetic ``__main__`` task so at least one example is captured.
"""

from __future__ import annotations

import os
import textwrap
from pathlib import Path

_SHIM_TEMPLATE = '''\
"""Auto-generated by `ai-evals bootstrap`. Do not edit.

Installed at {shim_dir}/sitecustomize.py and prepended to PYTHONPATH so Python
auto-imports it at interpreter startup, before any user import. Wires the
bootstrap capture wrappers to a JSONL sink the parent reads after this process
exits.
"""
from __future__ import annotations

import json
import os
import sys
import threading

_SINK_PATH = os.environ.get("AI_EVAL_BOOTSTRAP_SINK")
_REDACT = os.environ.get("AI_EVAL_BOOTSTRAP_REDACT", "1") == "1"
_TASK_SPECS = json.loads(os.environ.get("AI_EVAL_BOOTSTRAP_TASKS", "[]"))

_sink_lock = threading.Lock()
_sink_fh = None


def _open_sink():
    global _sink_fh
    if _sink_fh is None and _SINK_PATH:
        os.makedirs(os.path.dirname(_SINK_PATH), exist_ok=True)
        _sink_fh = open(_SINK_PATH, "a", encoding="utf-8")


def _write_example(example):
    if not _SINK_PATH:
        return
    _open_sink()
    with _sink_lock:
        if _sink_fh:
            _sink_fh.write(json.dumps(example, default=str, ensure_ascii=False))
            _sink_fh.write("\\n")
            _sink_fh.flush()


def _install():
    try:
        from ai_eval.bootstrap.wrappers import install_hooks
        from ai_eval.bootstrap.tracer import configure_sink, enter_task, exit_task, get_tracer
        install_hooks(redact_fields=_REDACT)
        configure_sink(sink=_write_example, redact_fields=["*"] if _REDACT else None)
        # Wrap each task spec's entry symbol as a top-level task boundary.
        for spec in _TASK_SPECS:
            _wrap_entry(spec)
        # If no task specs were provided, register a synthetic top-level task
        # so framework calls have somewhere to land.
        if not _TASK_SPECS:
            enter_task("__main__", {{"argv": sys.argv}})
    except Exception as exc:
        sys.stderr.write(f"ai-evals bootstrap: hook install failed: {{exc}}\\n")


def _wrap_entry(spec):
    import importlib
    mod_path = spec.get("file_path")
    entry = spec.get("entry") or "main"
    task_name = spec.get("name") or entry
    if not mod_path:
        return
    try:
        from ai_eval.bootstrap.tracer import enter_task, exit_task
    except Exception:
        return
    # Dotted entry support (D2/D3): ``Class.method`` resolves to the method on
    # the class and rebinds on the class (so instances pick up the wrapper).
    # A bare ``fn`` rebinds on the module as before.
    dotted = "." in entry and entry.rsplit(".", 1)[0]
    cls_name, method_name = (entry.rsplit(".", 1) if dotted else (None, entry))
    # Defer the actual import until the user imports their module; we patch
    # builtins.__import__ to wrap the entry symbol after the target module
    # loads. This is best-effort: if the user never imports the module, no
    # top-level task is recorded (framework calls still land in __main__).
    # NOTE: the import-hook parameter is ``name`` (the module being imported);
    # the spec's task name is ``task_name`` so the wrapper closure captures the
    # correct label (the old code shadowed ``name`` and recorded the module
    # name instead of the task name).
    import builtins
    import inspect
    _orig_import = builtins.__import__

    def _import(name, globals=None, locals=None, fromlist=(), level=0):
        mod = _orig_import(name, globals, locals, fromlist, level)
        try:
            target_mod = mod
            file_path = mod_path
            if hasattr(mod, "__file__") and mod.__file__ and file_path in mod.__file__:
                # Resolve the callable: dotted ``Class.method`` or bare ``fn``.
                if cls_name is not None:
                    owner = getattr(target_mod, cls_name, None)
                    fn = getattr(owner, method_name, None) if owner is not None else None
                    rebind_target = owner  # setattr on the class
                    rebind_attr = method_name
                else:
                    fn = getattr(target_mod, method_name, None)
                    rebind_target = target_mod  # setattr on the module
                    rebind_attr = method_name
                if callable(fn) and not getattr(fn, "_ai_eval_wrapped", False):
                    is_async = inspect.iscoroutinefunction(fn)

                    def _wrapped(*a, _fn=fn, _tn=task_name, **kw):
                        from ai_eval.bootstrap.tracer import enter_task, exit_task
                        enter_task(_tn, {{"args": list(a), "kwargs": kw}})
                        try:
                            return _fn(*a, **kw)
                        finally:
                            exit_task()

                    async def _wrapped_async(*a, _fn=fn, _tn=task_name, **kw):
                        from ai_eval.bootstrap.tracer import enter_task, exit_task
                        enter_task(_tn, {{"args": list(a), "kwargs": kw}})
                        try:
                            return await _fn(*a, **kw)
                        finally:
                            exit_task()

                    _wrapped._ai_eval_wrapped = True
                    _wrapped_async._ai_eval_wrapped = True
                    setattr(rebind_target, rebind_attr,
                            _wrapped_async if is_async else _wrapped)
        except Exception:
            pass
        return mod

    builtins.__import__ = _import


# Install at interpreter startup. sitecustomize is imported once, very early.
_install()
'''


def write_sitecustomize(
    shim_dir: Path,
    *,
    sink_path: Path,
    task_specs: list[dict],
    redact: bool = True,
) -> Path:
    """Write the sitecustomize shim to ``shim_dir`` and return its path.

    ``task_specs`` is a list of ``{name, file_path, entry}`` dicts (one per
    rubrics task) so the shim knows which callables to wrap as top-level task
    boundaries.
    """
    shim_dir.mkdir(parents=True, exist_ok=True)
    # The shim reads env vars at runtime; the parent sets them on the child
    # env via build_child_env. The redact/task_specs flow through env, not the
    # shim file itself, so the shim is content-identical across runs.
    content = textwrap.dedent(_SHIM_TEMPLATE).format(shim_dir=str(shim_dir))
    shim_path = shim_dir / "sitecustomize.py"
    shim_path.write_text(content, encoding="utf-8")
    return shim_path


def build_child_env(
    base_env: dict[str, str],
    shim_dir: Path,
    sink_path: Path,
    task_specs: list[dict],
    *,
    redact: bool = True,
) -> dict[str, str]:
    """Return a copy of ``base_env`` augmented for the bootstrap child.

    Prepends ``shim_dir`` to ``PYTHONPATH`` and sets the sink/tasks env vars.
    """
    import json

    env = dict(base_env)
    existing = env.get("PYTHONPATH", "")
    env["PYTHONPATH"] = (
        f"{shim_dir}{os.pathsep}{existing}" if existing else str(shim_dir)
    )
    env["AI_EVAL_BOOTSTRAP_SINK"] = str(sink_path)
    env["AI_EVAL_BOOTSTRAP_REDACT"] = "1" if redact else "0"
    env["AI_EVAL_BOOTSTRAP_TASKS"] = json.dumps(task_specs)
    # Ensure the ai_eval package is importable in the child (it usually is, but
    # a PYTHONPATH prepend can shadow site-packages ordering).
    return env


__all__ = ["build_child_env", "write_sitecustomize"]
